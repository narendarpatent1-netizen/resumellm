const BASE_URL = "http://localhost:3000/api/auth";

export async function login(email: string, password: string) {
    const res = await fetch(`${BASE_URL}/login`, {
        method: "POST",
        headers: {
            "Content-Type": "application/json"
        },
        body: JSON.stringify({ email, password })
    });

    return res.json();
}